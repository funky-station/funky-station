    public Action<uint?>? OnKeySelected;
    public Action<StationRecordFilterType, string>? OnFiltersChanged;
    public Action<ProtoId<SecurityStatusPrototype>>? OnStatusSelected;
    public Action<uint>? OnCheckStatus;
