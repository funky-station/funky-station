using System.Linq;
using Content.Client.Pinpointer.UI;
using Content.Client.UserInterface.Controls;
using Content.Shared._Funkystation.ResourceOverview.BUI;
using Robust.Client.AutoGenerated;
using Robust.Client.GameObjects;
using Robust.Shared.Utility;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Map;
using Robust.Shared.Maths;
using System.Numerics;

namespace Content.Client._Funkystation.ResourceOverview.UI;

[GenerateTypedNameReferences]
public sealed partial class ResourceOverviewWindow : FancyWindow
{
    [Dependency] private readonly IEntityManager _entManager = default!;
    private readonly SpriteSystem _spriteSystem;

    private static readonly SpriteSpecifier.Texture MapSiloTexture = new(new ResPath("/Textures/Interface/NavMap/beveled_hexagon.png"));
    private static readonly SpriteSpecifier.Texture MapLatheTexture = new(new ResPath("/Textures/Interface/NavMap/beveled_triangle.png"));
    private static readonly SpriteSpecifier.Rsi ListSiloTexture = new(new ResPath("/Textures/Structures/Machines/silo.rsi"), "silo");
    private static readonly SpriteSpecifier.Rsi ListLatheTexture = new(new ResPath("/Textures/Structures/Machines/autolathe.rsi"), "icon");

    public EntityUid Entity;

    private ResourceOverviewConsoleBoundInterfaceState? _state;
    private readonly HashSet<NetEntity> _expandedSilos = new();
    private readonly HashSet<NetEntity> _expandedLathes = new();
    private NetEntity? _highlightedEntity;

    public ResourceOverviewWindow()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);

        _spriteSystem = _entManager.System<SpriteSystem>();

        NavMap.ForceNavMapUpdate();

        MasterTabContainer.SetTabTitle(0, Loc.GetString("resource-overview-window-silos-tab"));
        MasterTabContainer.SetTabTitle(1, Loc.GetString("resource-overview-window-lathes-tab"));

        NavMap.TrackedEntitySelectedAction += OnMapEntitySelected;
    }

    public void SetEntity(EntityUid uid)
    {
        Entity = uid;

        NavMap.Owner = uid;

        var stationName = Loc.GetString("resource-overview-window-unknown-location");

        if (_entManager.TryGetComponent<TransformComponent>(uid, out var xform))
        {
            NavMap.MapUid = xform.GridUid;

            if (_entManager.TryGetComponent<MetaDataComponent>(xform.GridUid, out var stationMetaData))
                stationName = stationMetaData.EntityName;

            var msg = new FormattedMessage();
            msg.AddMarkupOrThrow(Loc.GetString("resource-overview-window-station-name", ("stationName", stationName)));
            StationName.SetMessage(msg);
        }
        else
        {
            StationName.SetMessage(stationName);
            NavMap.Visible = false;
        }
    }

    public void UpdateState(ResourceOverviewConsoleBoundInterfaceState state)
    {
        _state = state;

        NavMap.TrackedCoordinates.Clear();
        NavMap.TrackedEntities.Clear();

        if (NavMap is ResourceOverviewNavMapControl resourceNavMap)
        {
            resourceNavMap.Connections.Clear();
            resourceNavMap.Connections.AddRange(state.Connections);
        }

        RefreshMapBlips(state);

        PopulateList(SilosList, state.Silos, isSilo: true);
        PopulateList(LathesList, state.Lathes, isSilo: false);
    }

    private void RefreshMapBlips(ResourceOverviewConsoleBoundInterfaceState state)
    {
        NavMap.TrackedCoordinates.Clear();
        NavMap.TrackedEntities.Clear();

        foreach (var entry in state.Silos)
        {
            var coords = _entManager.GetCoordinates(entry.Coordinates);
            var texture = _spriteSystem.Frame0(MapSiloTexture);
            var color = _highlightedEntity == entry.NetEntity
                ? Color.Red
                : (entry.IsLowStock ? new Color(255, 150, 150) : Color.OrangeRed);
            NavMap.TrackedEntities[entry.NetEntity] = new NavMapBlip(coords, texture, color, false, true);
        }

        foreach (var entry in state.Lathes)
        {
            var coords = _entManager.GetCoordinates(entry.Coordinates);
            var texture = _spriteSystem.Frame0(MapLatheTexture);
            var color = _highlightedEntity == entry.NetEntity
                ? Color.Red
                : (entry.IsLowStock ? new Color(255, 150, 150) : Color.DodgerBlue);
            NavMap.TrackedEntities[entry.NetEntity] = new NavMapBlip(coords, texture, color, false, true);
        }
    }

    private void OnMapEntitySelected(NetEntity? netEntity)
    {
        if (_state == null || !netEntity.HasValue || !netEntity.Value.IsValid())
            return;

        var selected = netEntity.Value;

        var siloEntry = _state.Silos.FirstOrDefault(s => s.NetEntity == selected);
        if (siloEntry != null)
        {
            MasterTabContainer.CurrentTab = 0;
            _expandedSilos.Add(selected);
            _highlightedEntity = selected;
            RefreshMapBlips(_state);
            PopulateList(SilosList, _state.Silos, isSilo: true);
            return;
        }

        var latheEntry = _state.Lathes.FirstOrDefault(l => l.NetEntity == selected);
        if (latheEntry != null)
        {
            if (latheEntry.LinkedSiloNetEntity is { } linkedSilo)
            {
                MasterTabContainer.CurrentTab = 0;
                _expandedSilos.Add(linkedSilo);
                _highlightedEntity = linkedSilo;
            }
            else
            {
                MasterTabContainer.CurrentTab = 1;
                _expandedLathes.Add(selected);
                _highlightedEntity = selected;
            }
            RefreshMapBlips(_state);
            PopulateList(SilosList, _state.Silos, isSilo: true);
            PopulateList(LathesList, _state.Lathes, isSilo: false);
        }
    }

    private void PopulateList(BoxContainer container, ResourceOverviewEntry[] entries, bool isSilo)
    {
        var expanded = isSilo ? _expandedSilos : _expandedLathes;

        container.RemoveAllChildren();

        foreach (var entry in entries)
        {
            var row = CreateEntryRow(entry, isSilo, expanded.Contains(entry.NetEntity));

            var header = row.GetChild(0);
            if (header is Button headerButton)
            {
                headerButton.OnPressed += _ =>
                {
                    if (expanded.Contains(entry.NetEntity))
                        expanded.Remove(entry.NetEntity);
                    else
                        expanded.Add(entry.NetEntity);
                    _highlightedEntity = entry.NetEntity;
                    if (_state != null)
                        RefreshMapBlips(_state);
                    PopulateList(container, entries, isSilo);
                };
            }

            container.AddChild(row);
        }
    }

    private BoxContainer CreateEntryRow(ResourceOverviewEntry entry, bool isSilo, bool expanded)
    {
        var row = new BoxContainer
        {
            Orientation = BoxContainer.LayoutOrientation.Vertical,
            Margin = new Thickness(0, 2)
        };

        var headerButton = new Button
        {
            ToggleMode = false
        };

        var headerContent = new BoxContainer
        {
            Orientation = BoxContainer.LayoutOrientation.Horizontal,
            Children =
            {
                new TextureRect
                {
                    Texture = _spriteSystem.Frame0(isSilo ? ListSiloTexture : ListLatheTexture),
                    Stretch = TextureRect.StretchMode.KeepAspectCentered,
                    SetSize = new Vector2(16, 16),
                    Modulate = Color.White,
                    Margin = new Thickness(0, 0, 4, 0)
                },
                new Label
                {
                    Text = isSilo ? Loc.GetString("resource-overview-window-entry-silo") : Loc.GetString("resource-overview-window-entry-lathe"),
                    StyleClasses = { "LabelKeyText" }
                },
                new Label
                {
                    Text = entry.NearestBeaconName ?? string.Empty,
                    Margin = new Thickness(4, 0, 0, 0)
                }
            }
        };

        headerButton.AddChild(headerContent);
        row.AddChild(headerButton);

        if (expanded)
        {
            var materialsPanel = new BoxContainer
            {
                Orientation = BoxContainer.LayoutOrientation.Vertical,
                Margin = new Thickness(20, 2, 0, 0)
            };

            if (entry.Materials.Count == 0)
            {
                materialsPanel.AddChild(new Label
                {
                    Text = Loc.GetString("resource-overview-window-no-materials")
                });
            }
            else
            {
                foreach (var (mat, amount) in entry.Materials.OrderBy(m => m.Key))
                {
                    var sheets = amount / 100;
                    materialsPanel.AddChild(new Label
                    {
                        Text = $"{mat}: {sheets}",
                        Margin = new Thickness(0, 1)
                    });
                }
            }

            row.AddChild(materialsPanel);
        }

        return row;
    }
}
